    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String categoria = (String) jcbCategoria.getSelectedItem();
        String nombre = jtProducto.getText();
        double precio = 0;
        try {
            precio = Double.parseDouble(jtPrecio.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese un número válido");
            return;
        }
        if((!nombre.equals(""))&&(precio>0)){
           Object arr[] = {categoria,nombre, precio};
        modelo.addRow(arr);
        }else{
        JOptionPane.showMessageDialog(this, "Los campos deben estar completos");
        }    
        jtProducto.setText("");
        jtPrecio.setText("");
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

